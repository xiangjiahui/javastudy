package com.xjh.DefineclassandUse;
//创建一个类：包括成员变量和成员方法
public class Student {
    //成员变量（属性）
    String name = "向嘉晖";
    int age = 22;

    //成员方法（行为）
    public void eat(){
        System.out.println("吃饭");
    }

    public void sleep(){
        System.out.println("睡觉");
    }

    public void study(){
        System.out.println("学习");
    }
}
